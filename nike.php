Samenvatting:At Nike, the desire to be the best is a journey, not a destination-better is always temporary.<br>
Phaidon commemorates the company's influence with Nike: Better is Temporary, a landmark publication that charts Nike's transformation from rebellious upstart to global phenomenon.<br>
This immersive visual survey offers an unprecedented, behind-the-scenes exploration into Nike's ethos-driven design formula, placing industry-defining innovations and globally recognized products alongside previously unpublished designs, prototypes, insider stories, and more.<br>
Beginning with Breaking2, an introduction detailing Nike's 2017 attempt to facilitate a sub-two-hour marathon, the book lays out in five thematic chapters Nike's focus on performance, brand expression, collaboration, inclusive design, and sustainability.<br>
The book's extraordinary design also nods to its contents. The striking cover features overlapping silkscreened layers of Nike's proprietary Volt yellow and Hyperpunch pink colors overlaying an image of world-champion marathoner Eliud Kipchoge printed in a half-tone dot pattern.<br>
The book's spine, visible through the clear jacket, showcases a series of colored tabs that extend from its interior pages and which are referenced in the book's bonus chapter, Crafting Color.<br>
Combining 500 color illustrations with stories, insights, knowledge, passion, and history shared by Nike's remarkable team, Nike: Better is Temporary will serve as a manual of innovation and inspiration for generations to come.<br>
