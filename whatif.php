From the creator of the wildly popular xkcd.com, hilarious and informative answers to important questions you probably never thought to ask.<br>
Millions visit xkcd.com each week to read Randall Munroe's iconic webcomic.<br>
Fans ask him a lot of strange questions: How fast can you hit a speed bump, driving, and live? When (if ever) did the sun go down on the British Empire?<br>
