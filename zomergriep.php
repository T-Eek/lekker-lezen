Drie mannen staan op scherp in Amsterdam. Eén man leeft er, een andere bezoekt de stad en de derde ontvlucht haar.<br>
In Zomergriep stuurt een ambitieuze fotograaf e-mails aan een Amerikaanse vriend over zijn leven in een door een dodelijke griep belegerd Amsterdam.<br>
In Keerweer nr. 16 zoekt een journalist met detective-talent haast tegen zijn zin naar een verdwenen vrouw die ook nog zijn ex-geliefde is.<br>
En in Palmyra dobbert een zeezeiler voor de kust van een tropisch eiland.<br>
Hij leest een lange brief van zijn geliefde die terug gegaan is naar Amsterdam, de stad die hij is ontvlucht.<br>
